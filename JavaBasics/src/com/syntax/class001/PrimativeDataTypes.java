package com.syntax.class001;

public class PrimativeDataTypes {

	public static void main (String [] args) {
		
		
		/* * Primitive Data Types
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char
		 * boolean
		 * 		
		 */
		
		byte num = 10;
		
		short num1 = 100;
		
		int num2 = 1000;
		// use l or L  for long
		long num3 = 1000000125478l;
		
		//Decimal Number
		
		float  num4 = 12.9f;
		
		double num5 = 10.5;
		
		// Non Number Data Types
		
		char add = '+';
		char letter = 'a';
		
		// true or false (Yes/No)
		
		boolean hungry = true;
		boolean thirsty = false;
		
		
		
	}
}
